#!/bin/bash
#SBATCH --job-name=bwa_align
#SBATCH --output=scripts/03_alignment/bwa_align_%A_%a.out
#SBATCH --error=scripts/03_alignment/bwa_align_%A_%a.err
#SBATCH --ntasks=1
#SBATCH --cpus-per-task=8
#SBATCH --mem=48G
#SBATCH --time=24:00:00
#SBATCH --partition=general
#SBATCH --qos=general
#SBATCH --array=1-52%8

module load bwa/0.7.17
module load samtools/1.20

PROJ=/scratch/sballas/ISG5312FinalProject
TRIMMED=${PROJ}/results/02_qc/trimmed_fastq
OUTDIR=${PROJ}/results/03_alignment/bwa_align
REF=${PROJ}/genome/CanFam3.1.fa

mkdir -p ${OUTDIR}

SAMPLES=(
    SRR11352506 SRR11352507 SRR11352508 SRR11352509 SRR11352510
    SRR11352511 SRR11352512 SRR11352513 SRR11352514 SRR11352515
    SRR11352516 SRR11352517 SRR11352518 SRR11352519 SRR11352520
    SRR11352521 SRR11352522 SRR11352523 SRR11352524 SRR11352525
    SRR11352526 SRR11352527 SRR11352528 SRR11352529 SRR11352530
    SRR11352531 SRR11352532
    SRR11392157 SRR11392158 SRR11392159 SRR11392160 SRR11392161
    SRR11392162 SRR11392163 SRR11392164 SRR11392165 SRR11392166
    SRR11392167 SRR11392168 SRR11392169 SRR11392170 SRR11392171
    SRR11392172 SRR11392173 SRR11392174 SRR11392175 SRR11392177
    SRR11392178 SRR11392179 SRR11392180 SRR11392181 SRR11392182
)

SAMPLE=${SAMPLES[$((SLURM_ARRAY_TASK_ID - 1))]}
R1=${TRIMMED}/${SAMPLE}_1_trimmed.fastq.gz
R2=${TRIMMED}/${SAMPLE}_2_trimmed.fastq.gz
OUTBAM=${OUTDIR}/${SAMPLE}.sorted.bam

echo "Aligning: ${SAMPLE}"

RG="@RG\tID:${SAMPLE}\tSM:${SAMPLE}\tPL:ILLUMINA\tLB:${SAMPLE}_lib1\tPU:${SAMPLE}"

bwa mem \
    -t ${SLURM_CPUS_PER_TASK} \
    -R "${RG}" \
    ${REF} \
    ${R1} ${R2} \
  | samtools sort \
    -@ ${SLURM_CPUS_PER_TASK} \
    -o ${OUTBAM}

samtools index ${OUTBAM}

echo "Done: ${SAMPLE}"
